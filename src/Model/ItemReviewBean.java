package Model;

public class ItemReviewBean {
	int reviewUid;
	private String id;
	private String name;
	private String itemCode;
	private String itemName;
	private String itemFile;
	private String itemFile_s;
	private double starPoint;
	private String review;
	private String summary;
	private String reviewDate;
	
	public int getReviewUid() {
		return reviewUid;
	}
	public void setReviewUid(int reviewUid) {
		this.reviewUid = reviewUid;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getItemCode() {
		return itemCode;
	}
	public void setItemCode(String itemCode) {
		this.itemCode = itemCode;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getItemFile() {
		return itemFile;
	}
	public void setItemFile(String itemFile) {
		this.itemFile = itemFile;
	}
	public String getItemFile_s() {
		return itemFile_s;
	}
	public void setItemFile_s(String itemFile_s) {
		this.itemFile_s = itemFile_s;
	}
	public double getStarPoint() {
		return starPoint;
	}
	public void setStarPoint(double starPoint) {
		this.starPoint = starPoint;
	}
	public String getReview() {
		return review;
	}
	public void setReview(String review) {
		this.review = review;
	}
	public String getSummary() {
		return summary;
	}
	public void setSummary(String summary) {
		this.summary = summary;
	}
	public String getReviewDate() {
		return reviewDate;
	}
	public void setReviewDate(String reviewDate) {
		this.reviewDate = reviewDate;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
